--============================================================================
-- 1. Исходные данные
--============================================================================

-- Нагрузки, определенные по диаграмме растяжения (см. рисунок в задании)
-- P_max - максимальная нагрузка (соответствует временному сопротивлению)
-- P_k   - нагрузка в момент разрыва
P_values = {
    leather = { max = 530,   k = 250 }, -- Н
    rope    = { max = 220,   k = 170 }, -- Н
    wire    = { max = 160,   k = 130 }  -- Н
}

--============================================================================
-- 2. Расчеты
--============================================================================

-- Глобальная таблица для хранения всех результатов
results = {}

-- Итерация по всем образцам из протокола
for name, sample_data in pairs(protocol) do
    results[name] = {}
    local p = sample_data
    local r = results[name]

    -- 2.1. Расчет площадей поперечного сечения
    if name == "leather" then
        -- Образец прямоугольного сечения
        r.F0 = p.before.w * p.before.h
        r.Fk = p.after.w * p.after.h
    else
        -- Образцы круглого сечения
        r.F0 = math.pi * (p.before.d / 2)^2
        r.Fk = math.pi * (p.after.d / 2)^2
    end

    -- 2.2. Расчет характеристик прочности
    -- Временное сопротивление (предел прочности)
    r.sigma_v = P_values[name].max / r.F0
    -- Истинное сопротивление разрыву
    if r.Fk > 1e-12 then -- Проверка на деление на ноль
        r.sigma_k = P_values[name].k / r.Fk
    else
        r.sigma_k = 0 -- Для веревки, которая полностью распалась
    end

    -- 2.3. Расчет характеристик пластичности
    -- Относительное удлинение после разрыва
    r.delta = (p.after.l - p.before.l) / p.before.l * 100
    -- Относительное сужение после разрыва
    if r.F0 > 1e-12 then
        r.psi = (r.F0 - r.Fk) / r.F0 * 100
    else
        r.psi = 0
    end
end

--============================================================================
-- 3. Функции для форматирования вывода в LaTeX
--============================================================================

-- Форматирование числа в научном виде (например, 1.23 \cdot 10^4)
function format_sci(val, fmt)
    fmt = fmt or "%.2e"
    if val == nil or val == 0 then
        tex.print("---")
        return
    end
    local s = string.format(fmt, val)
    local base, exp = s:match("^(.*)e([+-]%d+)$")
    exp = tonumber(exp)
    tex.print(string.format("$%s \\cdot 10^{%d}$", base, exp))
end

-- Форматирование числа с плавающей точкой
function format_float(val, fmt)
    fmt = fmt or "%.2f"
    if val == nil then
        tex.print("---")
    else
        tex.print(string.format(fmt, val))
    end
end

-- Форматирование значения для ячейки таблицы (обертка)
function cell(val, formatter, ...)
    formatter = formatter or format_float
    tex.print(formatter(val, ...))
end