function y = ex1(x)
    % Определение нечетной функции на заданном отрезке x
    % в соответствии с рисунком из задания.
    %   Возвращает вектор y.
    %   Пример вызова: ex1(-10 : 0.01 : 10);

    y = (-2 <= x      &      x <= 2) .* (-1 * x) + ...
        ( 2  < abs(x) & abs(x) <= 6) .* (-2 * sign(x)) + ...
        ( 6  < abs(x)              ) .* ( 3 * sign(x));
end