function [x, y] = ex1()
    % Определение нечетной функции на отрезке [-10, 10]
    % в соответствии с рисунком из задания.
    % Возвращает векторы x и y, а также строит график функции.

    x = -10 : 0.01 : 10;

    y = (-2 <= x      &      x <= 2) .* (-1 * x) + ...
        ( 2  < abs(x) & abs(x) <= 6) .* (-2 * sign(x)) + ...
        ( 6  < abs(x)              ) .* ( 3 * sign(x));

    plot(x, y);
    grid on;
    xlabel('x'); ylabel('f(x)');
    title('Нечетная функция на [-10, 10]');
end